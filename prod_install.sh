# Helper for curl with optional token and validation
github_curl() {
    local url="$1"
    if [ -z "$url" ]; then
        return 0
    fi
    
    # Use array for curl arguments to prevent 'blank argument' issues
    local curl_opts=(-s -L)
    if [ -n "${GITHUB_TOKEN:-}" ]; then
        curl_opts+=(-H "Authorization: token $GITHUB_TOKEN")
    fi
    
    curl "${curl_opts[@]}" "$url"
}

